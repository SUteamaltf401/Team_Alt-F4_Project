document.getElementById('SET1_BTN').addEventListener('click', function(event) {
    var dropdown = document.querySelector('.btn-primary');
    dropdown.classList.toggle('active');
    
    var spinner_1 = document.getElementById('spinner_1');
    
    if (spinner_1.style.display === 'none') {
        spinner_1.style.display = 'inline-block';  
    } else {
        spinner_1.style.display = 'none';
    }
});
